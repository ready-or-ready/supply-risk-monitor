_PROMPT_TMPL = """당신은 석유화학 원재료 조달 전문가입니다.
아래 데이터를 바탕으로 조달담당자를 위한 실무 분석 리포트를 한국어로 작성해주세요.

## 현재 시장 데이터 ({mat})
- 위험지수: {score:.1f}점 ({grade}) — 등급 기준: 2020-2024년 과거 분포
- 국제유가압력: {oil:.1f}점 (가중치 25%)
- 환율압력: {fx:.1f}점 (가중치 15%)
- 수입가격압력: {imp:.1f}점 (가중치 20%) — 전월비 {imp_change}
- 수입구조취약성: {vuln:.1f}점 (가중치 20%)
- 지정학노출: {geo:.1f}점 (가중치 20%)
- 나프타 시나리오: {scenario_name} — {scenario_desc}
- NCC(에틸렌-나프타) 스프레드: {spread} USD/ton ({spread_text})

## 귀사 현황
- 재고 여유: {inv}
- 원가 영향 추정: {cost}
- 납품단가 조정 권고: {advice}

## 최근 주요 뉴스 (14일)
{headlines}

---
다음 3개 섹션으로 작성해주세요. 각 섹션은 2-3문장, 실무자가 바로 활용할 수 있는 표현으로.
반드시 "{mat}"를 품목명으로 명시하고, "석유화학 시장" 같은 일반 표현 대신 "{mat} 시장", "{mat} 수입단가" 등으로 구체적으로 작성하세요.

**① 시장 현황 분석**
현재 {mat} 위험지수와 주요 리스크 요인을 해석하고, 시장 흐름을 설명하세요.

**② 귀사 영향 분석**
{mat} 재고·원가·납품단가 데이터를 바탕으로 단기 영향을 평가하세요.

**③ 조달 전략 제언**
현재 {mat} 시장 상황에서 구체적인 조달 대응 방향을 제시하세요."""


def generate_ai_report(
    mat: str,
    r: dict,
    ncc_sum: dict,
    scenario: dict,
    inv_days,
    cost_impact,
    imp_change_str: str,
    advice: str,
    df_rss,
) -> tuple[str, bool]:
    """
    AI 종합 분석 생성.
    반환: (분석 텍스트, is_live) — is_live=False이면 하드코딩 fallback
    """
    api_key = st.secrets.get("OPENAI_KEY", "")
    if not api_key:
        return HARDCODED_ANALYSIS.get(mat, "분석 결과 없음"), False

    # 뉴스 헤드라인 최대 5건
    if df_rss is not None and not df_rss.empty:
        headlines = "\n".join(
            f"- {row['제목']}" for _, row in df_rss.head(5).iterrows()
        )
    else:
        headlines = "수집된 뉴스 없음"

    prompt = _PROMPT_TMPL.format(
        mat=mat,
        score=r["위험지수"],
        grade=r["등급"],
        oil=r["유가"],
        fx=r["환율"],
        imp=r["수입가격"],
        imp_change=imp_change_str,
        vuln=r["수입구조"],
        geo=r["지정학"],
        scenario_name=scenario.get("scenario_name", ""),
        scenario_desc=scenario.get("scenario_desc", ""),
        spread=ncc_sum.get("스프레드", ""),
        spread_text=ncc_sum.get("구간_텍스트", ""),
        inv=f"{inv_days:.0f}일" if inv_days else "미입력",
        cost=f"{cost_impact:+.1f}%" if cost_impact is not None else "미입력",
        advice=advice,
        headlines=headlines,
    )

    try:
        from openai import OpenAI
        client = OpenAI(api_key=api_key)
        msg = client.chat.completions.create(
            model="gpt-4o-mini",
            max_tokens=900,
            messages=[{"role": "user", "content": prompt}],
        )
        return msg.choices[0].message.content, True
    except Exception:
        return HARDCODED_ANALYSIS.get(mat, "분석 결과 없음"), False
